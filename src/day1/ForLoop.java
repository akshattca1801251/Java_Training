package day1;

public class ForLoop {
    public static void main(String[] args) {
        for(int i=0;i<=10;i++){
            if(i==0){
                System.out.println("FOr Loop Started");
            }
            System.out.println("The value of i is = "+i);
        }
        System.out.println("For loop ended");
    }
}
