package day1;

public class LiteralInJava {
    public static void main(String[] args) {
        //Literal - A constant value that can be assigned to variable
//        123 - integer literal
//        102.44-float literal
//        abc- string literal
        int num=12;
        char ch='A';
        long lg=5666666666666666l;//add l to make it long
        boolean bl=true;
        float f=12.44f;
        double d=22.33;//by default value will be double in float if you not add f
        String name="akshat";
        System.out.println(name);

    }
}
